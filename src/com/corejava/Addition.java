package com.corejava;

public class Addition 
{
// method 2
	int add(int num1 ,int num2)
	{
		return num1+num2;
	}
	
//	method with 3 
	int add(int num1 ,int num2 , int num3)
	{
		return num1+num2+num3;
	}
	
//	method with double 
	double add( double num1 ,double num2 , double num3)
	{
		return num1+num2+num3;
	}
}
