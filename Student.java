package com.corejava;


// pojo (plain old java object)
public class Student 
{

//	private variable(encapsulation)
	private String name;
	private int age ;
	
//	getter and setter
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
//	public setter and getter
	
	
}
